public class OperasiPerbandinga {

    public static void main(String[] args) {
        /**
         * Operator Perbandingan, nilainya boolean
         * > : Lebih dari
         * < : Kurang Dari
         * >= : Lebih Dari Sama Dengan
         * <= : Kurang Dari Sama Dengan
         * == : Sama Dengan
         * != : Tidak Sama Dengan
         */

        int value1 = 100;
        int value2 = 100;

        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);
        
        
        
    }
    
}
